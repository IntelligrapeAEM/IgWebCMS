package com.ig.igwebcms.core.constants;

/**
 * This interface contains all the constants related
 * to the file extension types ex. .png,.html etc.
 */
public final class FileExtConstants {

    /**
     * Instantiates a new FileExtConstants.
     */
    private FileExtConstants() {
        /* Empty Constructor */
    }

    /**
     * DOT_HTML is used to define a dot html file extension constant.
     */
    public static final String DOT_HTML = ".html";

    /**
     * DOT_PNG is used to define a dot html png file extension constant.
     */
    public static final String DOT_PNG = ".png";

    /**
     * DOT_JPG is used to define a dot jpg file extension constant.
     */
    public static final String DOT_JPG = ".jpg";

    /**
     * DOT_JPEG is used to define a dot jpeg file extension constant.
     */
    public static final String DOT_JPEG = ".jpeg";

    /**
     * DOT_BMP is used to define a dot bmp file extension constant.
     */
    public static final String DOT_BMP = ".bmp";

    /**
     * DOT_TIF is used to define a dot tif file extension constant.
     */
    public static final String DOT_TIF = ".tif";

    /**
     * DOT_GIF is used to define a dot gif file extension constant.
     */
    public static final String DOT_GIF = ".gif";

    /**
     * HTML is used to define a html file extension constant.
     */
    public static final String HTML = "html";

    /**
     * PNG is used to define a png file extension constant.
     */
    public static final String PNG = "png";

    /**
     * JPG is used to define a jpg file extension constant.
     */
    public static final String JPG = "jpg";

    /**
     * JPEG is used to define a jpeg file extension constant.
     */
    public static final String JPEG = "jpeg";

    /**
     * BMP is used to define a bmp file extension constant.
     */
    public static final String BMP = "bmp";

    /**
     * TIF is used to define a tif file extension constant.
     */
    public static final String TIF = "tif";

    /**
     * GIF is used to define a gif file extension constant.
     */
    public static final String GIF = "gif";
}
